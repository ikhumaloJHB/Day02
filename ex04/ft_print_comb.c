#include <unistd.h>

void ft_print_comb(void){

   int arr[3] = {48,48,48};

   while(arr[0] <= 57){

	   if((arr[0] < arr[1]) && (arr[1] < arr[2])){


		   write(1,&arr[0],1);
		   write(1,&arr[1],2);
		   write(1,&arr[2],3);

		   if(arr[0] != 55){
			   write(1,",",1);
		   }
	       
	       if(arr[0] != 55){
		        write(1," ",1);
		   }
       }

       if(arr[2]++ >= 57){
	      arr[2] = 48;
          arr[1] ++;
        }


        if(arr[1] == 58){
	      arr[1] = 48;
          arr[0]++;
		 }
    }	
}   


int main(){

  ft_print_comb();
  return 0;
};
